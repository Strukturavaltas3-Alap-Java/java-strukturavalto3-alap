package week14.day01.age;

public class Max {

    private int maxNumber;

    private int maxIndex;

    public Max(int maxNumber, int maxIndex) {
        this.maxNumber = maxNumber;
        this.maxIndex = maxIndex;
    }

    public int getMaxNumber() {
        return maxNumber;
    }

    public int getMaxIndex() {
        return maxIndex;
    }
}
